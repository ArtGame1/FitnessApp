    private final Context context;
    private final EmojiClickListener listener;

    public interface EmojiClickListener {
        void onEmojiClick(String emoji);
    }

    public EmojiAdapter(Context context, EmojiClickListener listener) {
        this.context = context;
        this.listener = listener;
    }

    @NonNull
    @Override
    public ViewHolder onCreateViewHolder(@NonNull ViewGroup parent, int viewType) {
        View view = LayoutInflater.from(parent.getContext())
                .inflate(R.layout.item_emoji, parent, false);
        return new ViewHolder(view);
    }

    @Override
    public void onBindViewHolder(@NonNull ViewHolder holder, int position) {
        holder.bind(emojis[position]);
    }

    @Override
    public int getItemCount() {
        return emojis.length;
    }

    class ViewHolder extends RecyclerView.ViewHolder {
        private final TextView emojiText;

        ViewHolder(@NonNull View itemView) {
            super(itemView);
            emojiText = itemView.findViewById(R.id.emoji_text);

            itemView.setOnClickListener(v -> {
                if (listener != null && getAdapterPosition() != RecyclerView.NO_POSITION) {
                    listener.onEmojiClick(emojis[getAdapterPosition()]);
                }
            });
        }

        void bind(String emoji) {
            emojiText.setText(emoji);
        }
    }
